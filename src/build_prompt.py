def build_rewrite_prompt(text: str) -> str:
    text_length = 1000
    return f"""
{text[:text_length]}

---

上記のWebスクレイピングによって得られたテキストです。テキストに含まれる概念をベースに、大学生向けの教科書のように有用で、教育的価値のある文章を書いて下さい。

以下の点を意識してください。
- 一般的な教科書で用いられる、フォーマルかつある程度親しみを持てる自然な日本語で書くこと。
- 正確な事実に基づいた文章を書くこと。
- 少数の最も有用だと思われる概念を深く扱うこと。
- 抽象的な表現をさけ、より具体的で有用な情報を多く含んだ文章を書くこと。
- 冗長な表現を使用せず、簡潔に書くこと。
- 読者に自然と批判的思考を促す内容、構成にすること。

ただし、テキストに含まれる以下の点は無視して文章を作成して下さい。
- Web由来のノイズ
- 広告文
- 一般的に、教科書に登場しない個別の製品名、会社名など

また、以下の制約を遵守して下さい。
- 読者に対して、「考えてみて下さい」「考察してみましょう」「批判的に見ると」といった、読者に問いかける、または何かを指示する表現を使用してはいけません。
- タイトル、導入句、画像は含めないでください。内容を直接書き始めてください。

それでは上記の指示に従って、文章を書き始めて下さい。
"""
