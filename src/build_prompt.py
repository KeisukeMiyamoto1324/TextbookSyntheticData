def build_rewrite_prompt(text: str) -> str:
    text_length = 1000
    return f"""
{text[:text_length]}

---

上記のWebスクレイピングによって得られたテキストです。テキストに含まれる概念をベースに、大学生向けの教科書の1単元として有用で、教育的価値のある文章を書いて下さい。
単に概念を列挙するのではなく、各概念を次へ進む前に詳しく展開してください。幅広く浅く扱うよりも、深い理解と包括的な探究を重視します。

以下の点を意識してください。
- 概念を深く扱うこと。
- 物語性があり、読者の思考を促す文体にすること。
- 1500文字以上、2000文字以下

ただし、テキストに含まれる以下の点は無視して文章を作成して下さい。
- Web由来のノイズ
- 広告文
- 有名でない個別の製品名、会社名などの固有名詞

タイトル、導入句、画像は含めないでください。内容を直接書き始めてください。
"""
